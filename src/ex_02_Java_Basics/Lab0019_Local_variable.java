package ex_02_Java_Basics;

public class Lab0019_Local_variable {

    public static void main(String[] args) {
        int a = 10; // Local variable 'a' declared and initialized
        byte b = 20; // Local variable 'b' declared and initialized
        char c = 'A'; // Local variable 'c' declared and initialized
        long l = 100L; // Local variable 'l' declared and initialized
        float d = 5.5f; // Local variable 'd' declared and initialized
        double e = 10.99; // Local variable 'e' declared and initialized
        boolean f = true; // Local variable 'f' declared and initialized

        System.out.println("Integer value: " + a);
        System.out.println("Byte value: " + b);
        System.out.println("Character value: " + c);
        System.out.println("Long value: " + l);
        System.out.println("Float value: " + d);
        System.out.println("Double value: " + e);
        System.out.println("Boolean value: " + f);
    }
}
