package ex_09_Switch;

public class Lab0070_Interview_Question {
    public static void main(String[] args) {
        char code = 'C';
        switch (code){
            default -> System.out.println("Hellloooo");
            case 'A' -> System.out.println("65");
            case 'B' -> System.out.println("66");
        }
    }
}
