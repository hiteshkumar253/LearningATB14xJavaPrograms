package ex_03_Java_Literals;

public class Lab0024_Boolean_Literals {
    public static void main(String[] args) {
        boolean a = true;
        boolean b = false;
        boolean is_Java_fun = true;
        boolean is_amit_married = false;

        System.out.println(a);  // Output: true
        System.out.println(b);  // Output: false
        System.out.println("Is Java fun? " + is_Java_fun);  // Output: Is Java fun? true
        System.out.println("Is amit married? " + is_amit_married);  // Output: Is amit married? false
    }
}
