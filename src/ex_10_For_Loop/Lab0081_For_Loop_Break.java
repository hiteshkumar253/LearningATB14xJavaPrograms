package ex_10_For_Loop;

public class Lab0081_For_Loop_Break {
    public static void main(String[] args) {
        for (int i = 1; i <=50 ; i++) {
            if (i==5){
                break;}
            System.out.println(i);  //as soon as I become 5  break will execute and come out of the loop
            }

        }
    }

